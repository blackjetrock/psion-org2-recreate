////////////////////////////////////////////////////////////////////////////////
//
// Clears the display
//
////////////////////////////////////////////////////////////////////////////////

void display_clear(void)
{
  for(int x=0; x<DISPLAY_NUM_CHARS; x++)
    {
      for(int y=0; y<DISPLAY_NUM_LINES; y++)
	{
	  printxy(x, y, ' ');
	}
    }
  
  print_home();
}

////////////////////////////////////////////////////////////////////////////////
//
//  Cursor positioned functions
//
////////////////////////////////////////////////////////////////////////////////

int curx = 0;
int cury = 0;

void print_home(void)
{
  curx = 0;
  cury = 0;
}

void print_nl(void)
{
  curx = 0;
  cury++;
  
  if( cury >= DISPLAY_NUM_LINES )
    {
      cury = 0;
    }
}

void print_str(char *s)
{
  printxy_str(curx,cury, s);

  curx += strlen(s);

  if( curx >= DISPLAY_NUM_CHARS )
    {
      print_nl();
    }
}
