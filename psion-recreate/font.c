int invert_byte(int b)
{
int r = 0;

  for(int i=0; i<8; i++)
    {
      r <<= 1;
      r |=(b & 1);
      b >>= 1;
    }
return(r);
}

////////////////////////////////////////////////////////////////////////////////
//
// Prints a character at (X,Y)
//
// ** internal functions that omly dump_lcd should use
//
////////////////////////////////////////////////////////////////////////////////

void i_printxy(int x, int y, int ch)
{
  int cx, cy;

  ch -= 0;
  
  cx = 127-(x * 6);
  cy = 3-y;
  Set_Page_Address(cy);
  Set_Column_Address(cx);

#if NEW_I2C
  
  i2c_start();
  
  // Send slave address with read bit
  i2c_send_byte(Write_Address);
  i2c_send_byte(0x40);

  for(int j=0; j<5; j++)
    {
      i2c_send_byte(invert_byte(font_5x7_letters[ch*5+(4-j)]));
    }
  i2c_send_byte(0);
  i2c_stop();
#else
  Start();
  
  SentByte(Write_Address);
  SentByte(0x40);
  
  for(int j=0; j<5; j++)
    {
      SentByte(invert_byte(font_5x7_letters[ch*5+(4-j)]));
    }
  SentByte(0);
  Stop();
#endif
}

////////////////////////////////////////////////////////////////////////////////
//
// Prints a string at (X,Y)
//

void i_printxy_str(int x, int y, char *str)
{
  while(*str)
    {
      i_printxy(x++, y, *(str++));
    }
}

////////////////////////////////////////////////////////////////////////////////
//
// Prints a value in hex as (X,Y)
//

void i_printxy_hex(int x, int y, int value)
{
  char hs[10];

  sprintf(hs, "%02X", value);
  i_printxy_str(x, y, hs);
}

